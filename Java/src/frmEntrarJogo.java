    public void reminder(int seconds) {
        timer = new Timer();
        timer.schedule(new RemindTask(), seconds*1000);
    }  

    class RemindTask extends TimerTask {
        public void run() {
            //System.out.println("Time's up!");
            //timer.cancel(); //Terminate the timer thread
            
            System.out.println(">> " + jogo.cliente.conectado);
            System.out.println(">> " + jogo.cliente.jogoIniciado);
            
            if ( jogo.cliente.conectado == true ) {
                lblStatus.setText("Status: Conectado ao Servidor!");
            }
            
            if ( jogo.cliente.jogoIniciado == true ) {
                formJogo = new frmJogo(jogo);
                formJogo.setVisible(true);
                
                timer.cancel();
                
                while (true) {
                    if ( !formJogo.isVisible() )
                        fechar();

                    try {
                        Thread.sleep(1000);
                    } catch (InterruptedException ex) {
                        Logger.getLogger(frmCriarJogo.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }

                // this.dispose();
            } else {
                reminder(3);
            }

        }
    }
